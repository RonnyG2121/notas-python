

usuario = input("Ingresa el usuario")
# Poniendo todo el string en mayúsculas:

print("El usuario es: ", usuario.upper())

# poner la cadena entera en minúsculas:
print("El usuario es: ", usuario.lower())
# Poniendo la primera letra de la cadena en mayúsculas:
print("El usuario es: ", usuario.capitalize())

# Comprobando si son dígitos o textos:
print(usuario.isdigit())
